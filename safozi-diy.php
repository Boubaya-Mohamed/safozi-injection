<ph>Les SAFOZI boxes, de XSBox à XLBox, sont des serveurs Cloud publics préconfigurés (Open-Source ou Windows) avec des ressources prédéfinies et dédiées.
Les serveurs se différencient par les propriétés du Cloud Computing qui suivent; la rapidité du déploiement, la facturation, la haute disponibilité, l’évolutivité verticale et horizontale.
</p>
